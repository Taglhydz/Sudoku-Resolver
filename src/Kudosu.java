    private static int[][] stringToSudoku(String sudokuString) {
        int[][] sudoku = new int[9][9];
        for (int y = 0; y < 9; y++) {
            char[] chars = new char[9];
            sudokuString.getChars(y * 9, y * 9 + 9, chars, 0);
            for (int x = 0; x < 9; x++) {
                sudoku[y][x] = Character.getNumericValue(chars[x]);
            }
        }
        return sudoku;
    }
